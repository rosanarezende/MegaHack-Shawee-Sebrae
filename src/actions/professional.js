export const setServices = (services) => ({
    type: "SET_SERVICES",
    payload: {
        services
    }
})

export const setLacations = (locations) => ({
    type: "SET_LOCATIONS",
    payload: {
        locations
    }
})

export const setProduct = (product) => ({
    type: "SET_PRODUCT",
    payload: {
      product
    }
})